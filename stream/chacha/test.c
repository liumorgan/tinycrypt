void dump_bytes (char *s, uint8_t b[], int len)
{
  int i;
  char c;
  
  printf ("\n%s=", s);
  for (i=0; i<len; i++) {
    if ((i & 7)==0) putchar('\n');
    if (i==len-1) c='\n'; else c=',';
    printf ("0x%02x%c ", b[i], c);
  }
}
